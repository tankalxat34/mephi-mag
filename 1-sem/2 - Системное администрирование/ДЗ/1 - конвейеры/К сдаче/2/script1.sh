#!/bin/bash
# Часть 2. Задание 1.
# Создайте Bash-скрипт, который будет проверять доступность нескольких хостов 
# в вашей локальной сети. Список хостов (IP-адреса или доменные имена) должен быть 
# задан в переменной в начале скрипта. Скрипт должен последовательно проверять 
# каждый хост на доступность с использованием команды ping. Если хост доступен, 
# выведите сообщение «Хост [ИМЯ ХОСТА] доступен», если нет — выведите сообщение 
# «Хост [ИМЯ ХОСТА] недоступен». В конце скрипта выведите общее количество 
# доступных и недоступных хостов.

# Список хостов (IP или доменные имена)
HOSTS=("192.168.0.100" "192.168.0.202" "192.168.0.101")

# Счётчики
available=0
unavailable=0

# Перебираем каждый хост
for host in "${HOSTS[@]}"; do
    if ping -c 1 -W 3 "$host" > /dev/null 2>&1; then
        echo "Хост [$host] доступен"
        ((available++))
    else
        echo "Хост [$host] недоступен"
        ((unavailable++))
    fi
done

# Выводим итог
echo ""
echo "Итог:"
echo "Доступных хостов: $available"
echo "Недоступных хостов: $unavailable"